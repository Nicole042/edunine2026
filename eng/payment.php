            <h3>Who is considered a Guest?</h3>
            <p>
                A <strong>Guest</strong> is <strong>not</strong> an author or participant and cannot attend technical
                sessions or present papers.
            </p>
            <ul>
                <li>A Guest is only an accompanying person of an author or registered participant (often a spouse or
                    family member).</li>
                <li>Guests may attend <strong>social activities only</strong>: welcome cocktail, tours and coffee
                    breaks.</li>
                <li>Guests do <strong>not</strong> receive certificates.</li>
            </ul>
            <p>
                For technical sessions and paper presentation you must register as a participant (author or co-author),
                not as a Guest.
            </p>
